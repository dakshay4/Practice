package com.stpl.Hotels;

public class Hotel {

		private Integer sno;
		
		private String c_in;
		private String c_out;
		private Integer standard;
		private Integer deluxe;
		private Integer super_deluxe;
		private Integer suite;
		
		
		public Integer getSno() {
			return sno;
		}
		public void setSno(Integer sno) {
			this.sno = sno;
		}
		
		public String getC_in() {
			return c_in;
		}
		public void setC_in(String c_in) {
			this.c_in = c_in;
		}
		public String getC_out() {
			return c_out;
		}
		public void setC_out(String c_out) {
			this.c_out = c_out;
		}
		public Integer getStandard() {
			return standard;
		}
		public void setStandard(Integer standard) {
			this.standard = standard;
		}
		public Integer getDeluxe() {
			return deluxe;
		}
		public void setDeluxe(Integer deluxe) {
			this.deluxe = deluxe;
		}
		public Integer getSuper_deluxe() {
			return super_deluxe;
		}
		public void setSuper_deluxe(Integer super_deluxe) {
			this.super_deluxe = super_deluxe;
		}
		public Integer getSuite() {
			return suite;
		}
		public void setSuite(Integer suite) {
			this.suite = suite;
		}
		
		
		
		
}
